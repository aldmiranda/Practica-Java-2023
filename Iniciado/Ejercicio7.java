package Iniciado;

public class Ejercicio7 {
    public static void main(String[] args) {
        double numreal1, numreal2;
        numreal1 = 2.5; 
        numreal2 = 1.5;

        System.out.println(numreal1 * numreal2);

        int num1, num2;
        num1 = 10;
        num2 = 15;

        System.out.println(num1 * num2);

        System.out.println((numreal1 * numreal2) + (num1 * num2)); 

        // el resultado es 153.75 y es un número real porque la suma de ambos siempre dará como resultado 
        // un número con decimales, es decir un real.
    }
}
